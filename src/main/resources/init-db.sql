CREATE SCHEMA IF NOT EXISTS PUBLIC;

CREATE TABLE IF NOT EXISTS PUBLIC.TASK (
	ID_TASK BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	TX_NAME VARCHAR(100) NOT NULL,
	TX_DESCRIPTION VARCHAR(300),
	CD_STATUS SMALLINT NOT NULL,
	IN_PUBLIC_TASK BPCHAR(1) DEFAULT 'Y' CHECK(IN_PUBLIC_TASK = 'Y' OR IN_PUBLIC_TASK = 'N'),
	CD_RESPONSIBLE_USER VARCHAR(40) NOT NULL
);